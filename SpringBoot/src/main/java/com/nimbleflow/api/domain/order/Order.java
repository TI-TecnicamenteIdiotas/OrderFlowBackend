package com.nimbleflow.api.domain.order;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import com.nimbleflow.api.config.DynamoDBConfig.ZonedDateTimeConverter;
import com.nimbleflow.api.domain.order.enums.PaymentMethod;
import com.nimbleflow.api.domain.product.ProductDTO;
import com.nimbleflow.api.utils.dynamodb.converters.ListProductDTOConverter;
import com.nimbleflow.api.utils.dynamodb.converters.UUIDConverter;
import lombok.*;

import java.time.ZonedDateTime;
import java.util.List;
import java.util.UUID;

@Data
@Builder
@ToString
@EqualsAndHashCode
@NoArgsConstructor
@AllArgsConstructor
@DynamoDBTable(tableName = "order")
public class Order {

    @DynamoDBHashKey
    @DynamoDBTypeConverted(converter = UUIDConverter.class)
    @DynamoDBAutoGeneratedKey
    private UUID id;

    @DynamoDBAttribute
    @DynamoDBTypeConverted(converter = UUIDConverter.class)
    private UUID tableId;

    @DynamoDBAttribute
    @DynamoDBTypeConverted(converter = ZonedDateTimeConverter.class)
    private ZonedDateTime createdAt;

    @DynamoDBAttribute
    @DynamoDBTypeConvertedEnum
    private PaymentMethod paymentMethod;

    @DynamoDBAttribute
    @DynamoDBTypeConverted(converter = ListProductDTOConverter.class)
    private List<ProductDTO> products;

    @DynamoDBAttribute
    private boolean active;

}